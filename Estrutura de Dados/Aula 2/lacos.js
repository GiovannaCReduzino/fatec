// e exibe o valor de i no console enquanto i for menor que 10; 
// i é iniciado com 0, portanto o código a seguir exibirá os valores de 0 a 9

for (var i = 0; i < 10; i++) {
    console.log(i);
}

// mostrará os valores de 0 a 9
var i = 0;
while (i < 10) {
    console.log(i);
    i++;
}


// mostra os numeros de 0 a 9, porém usa o do...while
var i = 0;
do {
    console.log(i);
    i++;
} while (i < 10);