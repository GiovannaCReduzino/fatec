// ; é opcional
var num = 1; // declara um número
num = 3; // atualizando a váriavel já criada
var price = 1.5; // declarou um numero decimal (float)
var myName = 'Elisa'; // string
var trueValue = true; // booleano
var nullVar = null; // valor nulo
var und; // valor indefinido (undefined)

// para que o código apareça em ferramentas do desenvolvedor (crtl + shift + i)
// aparecem na aba console (por isso usa console.log)
console.log('Num =  ' + num);
console.log('myName = ' + myName);
console.log('trueValue =  ' + trueValue);
console.log('Price = ' + price);
console.log('nullVar = ' + nullVar);
console.log('und = ' + und);

